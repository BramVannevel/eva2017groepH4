package com.projecten3.eva.Model;

public class Categorieen {
    public String id;
    public String naam;

    public Categorieen(String id, String naam) {
        this.id = id;
        this.naam = naam;
    }

    public String getId() {
        return id;
    }

    public String getNaam() {
        return naam;
    }

}
